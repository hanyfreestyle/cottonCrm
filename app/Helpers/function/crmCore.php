if (!function_exists('TicketDateFrom')) {
    function TicketDateFrom($date) {
        $diff_h = Carbon::parse($date)->diff(Carbon::now());
        $diff = Carbon::parse($date)->diffForHumans(Carbon::now());
        if ($diff_h->d > 0) {
            return " ( " . $diff . " )";
        } else {
            return null;
        }
    }
}

