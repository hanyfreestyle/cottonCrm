if (!function_exists('CardOpenState')) {
    function CardOpenState($agent,$state=null) {
        if($state){
            $open = $state ;
        }else{
            if($agent->isDesktop()){
                $open = true ;
            }else{
                $open = false ;
            }
        }
        return $open;
    }
}
